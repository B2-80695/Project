# Write a program to accept three integer numbers and find its average.

str_num1 = input("Enter 1st number:")
num1 = int(str_num1)

str_num2 = input("Enter 2nd number:")
num2 = int(str_num2)

str_num3 = input("Enter 3rd number:")
num3 = int(str_num3)

print(f"Average of three numbers = {(num1 + num2 + num3)/3}")